class Math {
    constructor() {}

    add(a, b) {
        return a + b;
    }

    substract(a, b) {
        return a - b;
    }

    multiply(a, b) {
        return a * b;
    }

    divide(a, b) {
        return a/b;
    }

    percentage(a, b) {
        return (a * b) / 100;
    }
}

export default Math;