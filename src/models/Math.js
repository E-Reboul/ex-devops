class Maths {
    constructor() {}

    add(a, b) {
        return a + b;
    }

    substract(a, b) {
        return a - b;
    }

    multiply(a, b) {
        return a * b;
    }

    divide(a, b) {
        return a/b;
    }

    percentage(a, b) {
        return (a * b) / 100;
    }

    cos(a) {
        return Math.cos(a);
    }

    sin(a) {
        return Math.sin(a);
    }

    tan(a) {
        return Math.tan(a);
    }

    pow(a, b) {
        return Math.pow(a, b);
    }

    sqrt(a) {
        return Math.sqrt(a);
    }
}

export default Maths;